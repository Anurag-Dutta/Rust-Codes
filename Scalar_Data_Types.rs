fn main()
{
    let a:u32 = 10; // Declares an unsigned integer of 32 bits.
    let b:u64 = 11; // Declares an unsigned integer of 64 bits.
    let c:i32 = 123456789; // Declares a signed integer of 32 bits.
    let d:i64 = 163165656355; // Declares a signed integer of 64 bits.
    let e:f32 = 1.23; // Declares a floating point number of 32 bits.
    let f:f64 = 1.2563; // Declares a floating point number of 64 bits.
    let g:bool = false; // Declares a boolean value.
    let h:char = 'a'; // Declares a character value.
    let i = "Anurag"; // Declares a string.
    println!("The value of a is: {}",a);
    println!("The value of b is: {}",b);
    println!("The value of c is: {}",c);
    println!("The value of d is: {}",d);
    println!("The value of e is: {}",e);
    println!("The value of f is: {}",f);
    println!("The value of g is: {}",g);
    println!("The value of h is: {}",h);
    println!("The value of i is: {}",i);
}